package duck.craftffxiv;

public class CsvEntry {
	private int index;
	private String item;
	private int needed;
	
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public int getNeeded() {
		return needed;
	}
	public void setNeeded(int needed) {
		this.needed = needed;
	}
}
